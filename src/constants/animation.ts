/**
 * アニメーション関連の定数
 *
 * @remarks
 * CSS トランジションのタイミング制御に使用する定数です。
 */

/**
 * デフォルトのトランジション時間（ミリ秒）
 *
 * @remarks
 * CSS の transition-duration を読み取れなかった場合や、
 * 値が不正な場合のフォールバック値として使用されます。
 * 一般的なUIアニメーションの標準的な長さである200msを使用しています。
 */
export const DEFAULT_TRANSITION_MS = 200;

/**
 * フォールバックの安全マージン（ミリ秒）
 *
 * @remarks
 * onTransitionEnd イベントが発火しない場合のフォールバックタイマーに
 * 追加する安全マージンです。
 *
 * CSS transition-duration に加えてこの時間を追加することで、
 * トランジションが確実に完了した後にイベントを発火させます。
 *
 * 50ms は以下の理由で選定されています：
 * - ブラウザのフレームレート（60fps = 16.67ms/frame）の約3フレーム分
 * - トランジション完了の検出遅延を吸収できる十分な時間
 * - ユーザー体験に影響しない程度の短い時間
 */
export const FALLBACK_MARGIN_MS = 50;
